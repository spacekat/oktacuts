import React from 'react'

export type DataStateType = [
  chrome.declarativeNetRequest.Rule[],
  React.Dispatch<React.SetStateAction<chrome.declarativeNetRequest.Rule[]>>
]

export const getRule = async (id: number) => {
  return (await chrome.declarativeNetRequest.getDynamicRules()).find(
    (val) => val.id === id
  )
}

export const getRules = async () => {
  return await chrome.declarativeNetRequest.getDynamicRules()
}

type UpdateRuleParams = {
  id: number
  shortUrl: string
  longUrl: string
}

export const updateRule = async ({
  id,
  shortUrl,
  longUrl,
}: UpdateRuleParams) => {
  return await chrome.declarativeNetRequest.updateDynamicRules({
    removeRuleIds: [id],
    addRules: [
      {
        id,
        priority: 1,
        condition: {
          urlFilter: '||' + shortUrl + '/|',
          resourceTypes: [chrome.declarativeNetRequest.ResourceType.MAIN_FRAME],
        },
        action: {
          type: chrome.declarativeNetRequest.RuleActionType.REDIRECT,
          redirect: {
            url: longUrl,
          },
        },
      },
    ],
  })
}

export const deleteRule = async (id: number) => {
  return chrome.declarativeNetRequest.updateDynamicRules({
    removeRuleIds: [id],
  })
}

